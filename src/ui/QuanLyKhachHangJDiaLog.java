    void fillTable() {
        DefaultTableModel model = (DefaultTableModel) tblKhachHang.getModel();
        model.setRowCount(0);
        try {
            List<KhachHang> list = dao.selectAll();
            for (KhachHang kh : list) {
                Object[] row = {
                    kh.getMAKH(),
                    kh.getTENKH(),
                    kh.getDIACHI(),
                    kh.getSDT(),
                    kh.getNGAYSINH(),
                    kh.getEMAIL(),
                    kh.getGIOITINH() ? "Nam" : "Nữ",};
                model.addRow(row);
            }
        } catch (Exception e) {
            MsgBox.alert(this, "Lỗi truy vấn" + e);
        }
    }

    void edit() {
        Integer manv = (Integer) tblKhachHang.getValueAt(this.row, 0);
        KhachHang nv = dao.selectById(manv);
        this.setForm(nv);
        this.updateStatus();
    }

