    void delete() {
        if (MsgBox.confirm(this, "Bạn thực sự muốn xoá khách hàng này ?")) {
            Integer makh = Integer.valueOf(txtMaKhachHang.getText());
            try {
                dao.delete(makh);
                this.fillTable();
                this.clearForm();
                MsgBox.alert(this, "Xoá thành công");
            } catch (Exception e) {
                MsgBox.alert(this, "Xoá thất bại");
            }
        }
    }

    void update() {
        if (!validates()) {
            return;
        } else {
            KhachHang model = this.getForm();
            try {
                dao.update(model);
                this.fillTable();
                MsgBox.alert(this, "Cập nhật thành công");
            } catch (Exception e) {
                MsgBox.alert(this, "Cập nhật thất bại" + e);
            }
        }
    }

    void insert() {
        if (!validates()) {
            return;
        } else {
            KhachHang model = getForm1();
            try {
                dao.insert(model);
                this.fillTable();
                this.clearForm();
                MsgBox.alert(this, "Thêm mới thành công");
            } catch (Exception e) {
                MsgBox.alert(this, "Thêm mới thất bại" + e);
            }
        }
    }

      boolean validates() {
        if (txtTenKhachHang.getText().equals("")) {
            MsgBox.alert(this, "Tên khách hàng không được rỗng");
            txtTenKhachHang.requestFocus();
            return false;
        }

        if (txtSDT.getText().equals("")) {
            MsgBox.alert(this, "Số điện thoại không được rỗng");
            txtSDT.requestFocus();
            return false;

        }
        boolean flag;
        do {
            String phonePattern = "((09|03|07|08|05)+([0-9]{8}))|(1900+([0-9]{4,8}))";
            flag = txtSDT.getText().matches(phonePattern);
            if (!flag) {
                JOptionPane.showMessageDialog(this, "Số điện thoại không hợp lệ");
                txtSDT.requestFocus();
                return false;
            }
        } while (!flag);

        do {
            String phonePattern = "(0?[1-9]|[12][0-9]|3[01])/(0?[1-9]|1[012])/((19|20)[0-9]{2})";
            flag = txtNgaySinh.getText().matches(phonePattern);
            if (!flag) {
                JOptionPane.showMessageDialog(this, "Ngày sinh không hợp lệ");
                txtNgaySinh.requestFocus();
                return false;
            }
        } while (!flag);

        do {
            if (!txtEmail.getText().equals("")) {
                String emailPattern = "(\\w+@\\w+[.]\\w+[.]\\w+)|(\\w+@\\w+[.]\\w+)";
                flag = txtEmail.getText().matches(emailPattern);
                if (!flag) {
                    JOptionPane.showMessageDialog(this, "Email không hợp lệ");
                    txtEmail.requestFocus();
                    return false;
                }
            }
        } while (!flag);
        return true;
    }


    void first() {
        if (tblKhachHang.getValueAt(this.row, 0) != null) {

            this.row = 0;
            tblKhachHang.setRowSelectionInterval(row, row);
            this.edit();
        }

    }

    void prev() {
        if (this.row > 0) {
            this.row--;
            tblKhachHang.setRowSelectionInterval(row, row);
            this.edit();
        }
    }

    void next() {
        if (this.row < tblKhachHang.getRowCount() - 1) {
            this.row++;
            tblKhachHang.setRowSelectionInterval(row, row);
            this.edit();
        }
    }

    void last() {
        this.row = tblKhachHang.getRowCount() - 1;
        tblKhachHang.setRowSelectionInterval(row, row);
        this.edit();
    }

    void updateStatus() {
        boolean edit = (this.row >= 0);
        boolean first = (this.row == 0);
        boolean last = (this.row == tblKhachHang.getRowCount() - 1);
        // Trạng thái form

        btnThem.setVisible(!edit);
        btnSua.setVisible(edit);
        btnXoa.setVisible(edit);

        // Trạng thái điều hướng
        btnFirst.setVisible(edit && !first);
        btnPrev.setVisible(edit && !first);
        btnNext.setVisible(edit && !last);
        btnLast.setVisible(edit && !last);
    }
